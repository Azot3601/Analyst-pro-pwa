const good = (id: string, label: string, unlocks: string[], next: string | null, feedback: string): InterviewOption => ({
  id,
  label,
  unlocksRequirementIds: unlocks,
  nextNodeId: next,
  isGoodQuestion: true,
  feedback
});
const bad = (id: string, label: string, next: string | null, feedback: string): InterviewOption => ({
  id,
  label,
  unlocksRequirementIds: [],
  nextNodeId: next,
  isGoodQuestion: false,
  feedback
});

