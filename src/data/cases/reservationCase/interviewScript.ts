export type InterviewRequirement = { id: string; text: string; priority: 'must' | 'should' };

export type InterviewOption = {
  id: string;
  label: string;
  unlocksRequirementIds: string[];
  nextNodeId: string | null;
  isGoodQuestion: boolean;
  feedback: string;
};

export type InterviewNode = {
  id: string;
  speakerLine: string;
  questionOptions: InterviewOption[];
};

export type InterviewScript = {
  task: string;
  startNodeId: string;
  requirements: InterviewRequirement[];
  nodes: InterviewNode[];
};

