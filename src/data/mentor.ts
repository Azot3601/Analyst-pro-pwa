// Софи, Чтица свитков — эльфийка-наставница Великого Архива Златограда.
// Тёплая, иронично-добрая «учительница-следопыт»: подсказывает вопросом,
// никогда не стыдит, последняя подсказка указывает на механизм, но не на ответ.

export const mentor = {
  name: 'Софи',
  title: 'Чтица свитков',
  tagline: 'Данные редко лгут — они просто говорят тихо.',
  // Реплики-ободрения на ошибку (выбираются по настроению, без стыда).
  encouragement: [
    'Спокойно — нужный архив ты открыл, осталась мелочь.',
    'Почти. Свиток шепчет, что одно условие сбоит — глянь ещё раз.',
    'Не та печать, но ты рядом. Давай сузим поиск.',
    'Тёплый след. Ещё один взгляд на условие — и сойдётся.'
  ],
  // Реплики на успех.
  praise: [
    'Вот теперь чисто. Архивариус отпустил тревожный колокол.',
    'Архив вспомнил правду. Ещё одна лампа в городе зажглась.',
    'Точно и красиво. Так и пишутся настоящие свитки.'
  ]
} as const;

let praiseIdx = 0;
let cheerIdx = 0;

export const mentorPraise = () => mentor.praise[praiseIdx++ % mentor.praise.length];
export const mentorEncouragement = () => mentor.encouragement[cheerIdx++ % mentor.encouragement.length];

// Префикс реплики голосом Софи.
export const mentorSays = (text: string) => `${mentor.name}: ${text}`;
