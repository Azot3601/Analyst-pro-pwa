// Модуль «Анализ требований: Элиситация → User Stories».
// PoC «петли аналитика» (шаги 1 и 3), полностью офлайн и детерминированный.
// См. docs/learning-architecture.md.

type RequirementsLevel = 'junior' | 'junior+' | 'middle';

type RequirementsTaskBase = {
  id: string;
  title: string;
  level: RequirementsLevel;
  estimatedFocus: string;
  step: string;
  brief: string;
  learningGoal: string;
  realLife: string;
  taskText: string;
  hints: Hint[];
  explanation: string;
  expertSolution: string[];
  reflectionPrompt: string;
  relatedKnowledgeIds: string[];
};

export type ClassificationTask = RequirementsTaskBase & {
  kind: 'classification';
  rule: ClassificationRule;
};
export type QuestionsTask = RequirementsTaskBase & { kind: 'questions'; rule: QuestionsRule };
export type StoryTask = RequirementsTaskBase & { kind: 'story'; rule: StoryRule };

export type RequirementsTask = ClassificationTask | QuestionsTask | StoryTask;

const hints = (id: string, texts: string[]): Hint[] =>
  texts.map((text, index) => ({
    id: `${id}-h${index + 1}`,
    level: Math.min(5, index + 1),
    title: index === 0 ? 'Лёгкий намёк' : index === texts.length - 1 ? 'Проверка риска' : 'Направление',
    text
  }));

