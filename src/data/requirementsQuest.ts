export type ClassificationTask = RequirementsTaskBase & {
  kind: 'classification';
  rule: ClassificationRule;
};
export type QuestionsTask = RequirementsTaskBase & { kind: 'questions'; rule: QuestionsRule };
export type StoryTask = RequirementsTaskBase & { kind: 'story'; rule: StoryRule };

export type RequirementsTask = ClassificationTask | QuestionsTask | StoryTask;

